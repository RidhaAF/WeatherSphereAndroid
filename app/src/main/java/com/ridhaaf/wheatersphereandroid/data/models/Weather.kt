package com.ridhaaf.wheatersphereandroid.data.models

data class Weather(
    val id: Long,
    val appTemp: Double,
    val aqi: Int,
    val cityName: String,
    val clouds: Int,
    val countryCode: String,
    val datetime: String,
    val dewpt: Int,
    val dhi: Double,
    val dni: Double,
    val elevAngle: Double,
    val ghi: Double,
    val gust: Double,
    val hAngle: Int,
    val lat: Double,
    val lon: Double,
    val obTime: String,
    val pod: String,
    val precip: Double,
    val pres: Double,
    val rh: Int,
    val slp: Double,
    val snow: Int,
    val solarRad: Double,
    val sources: List<String>,
    val stateCode: String,
    val station: String,
    val sunrise: String,
    val sunset: String,
    val temp: Double,
    val timezone: String,
    val ts: Int,
    val uv: Double,
    val vis: Int,
    val weather: WeatherDescription,
    val windCdir: String,
    val windCdirFull: String,
    val windDir: Int,
    val windSpd: Double,
) {
    companion object {
        fun emptyWeather() = Weather(
            id = 0,
            appTemp = 0.0,
            aqi = 0,
            cityName = "",
            clouds = 0,
            countryCode = "",
            datetime = "",
            dewpt = 0,
            dhi = 0.0,
            dni = 0.0,
            elevAngle = 0.0,
            ghi = 0.0,
            gust = 0.0,
            hAngle = 0,
            lat = 0.0,
            lon = 0.0,
            obTime = "",
            pod = "",
            precip = 0.0,
            pres = 0.0,
            rh = 0,
            slp = 0.0,
            snow = 0,
            solarRad = 0.0,
            sources = listOf(),
            stateCode = "",
            station = "",
            sunrise = "",
            sunset = "",
            temp = 0.0,
            timezone = "",
            ts = 0,
            uv = 0.0,
            vis = 0,
            weather = WeatherDescription.emptyWeatherDescription(),
            windCdir = "",
            windCdirFull = "",
            windDir = 0,
            windSpd = 0.0,
        )
    }
}